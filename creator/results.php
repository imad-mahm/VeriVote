<?php
declare(strict_types=1);

require_once dirname(__DIR__) . '/includes/bootstrap.php';

$eventId = (int) ($_GET['event'] ?? $_POST['event_id'] ?? 0);
$event = fetch_event_by_id($eventId);

if (!$event) {
    flash('error', 'Event not found.');
    redirect('/creator/dashboard.php');
}

require_event_permission($eventId, 'view_results');

if (is_post_request()) {
    verify_csrf_or_fail();

    try {
        $snapshot = create_result_snapshot_record($eventId, (int) current_user()['id'], 'manual');
        write_audit_log('snapshot_created', 'result_snapshots', $snapshot['integrity_hash'], 'Result snapshot generated.', $eventId);
        flash('success', 'Snapshot generated. Integrity hash: ' . $snapshot['integrity_hash']);
    } catch (Throwable $exception) {
        flash('error', 'Could not generate snapshot. ' . $exception->getMessage());
    }

    redirect('/creator/results.php?event=' . $eventId);
}

$results = compute_event_results($eventId);
$snapshot = latest_result_snapshot($eventId);
$approvedCount = (int) (fetch_one('SELECT COUNT(*) AS aggregate FROM voter_event_submissions WHERE event_id = :event_id AND status = "approved"', ['event_id' => $eventId])['aggregate'] ?? 0);
$usedTokenCount = (int) (fetch_one('SELECT COUNT(*) AS aggregate FROM voting_tokens WHERE event_id = :event_id AND status = "used"', ['event_id' => $eventId])['aggregate'] ?? 0);

$pageTitle = 'Results and Analytics';
$pageHeading = 'Results and Analytics';
$pageDescription = 'Review live tallies, turnout indicators, and integrity snapshots.';
$isDashboard = true;
$sidebarContext = current_role_slug() ?? 'event_creator';
$activeEventTool = 'results';
$eventContextId = $eventId;

include dirname(__DIR__) . '/includes/header.php';
?>
<section class="stats-grid">
    <article class="stat-box">
        <strong><?= e((string) $results['total']); ?></strong>
        <p>Total ballots</p>
    </article>
    <article class="stat-box">
        <strong><?= e((string) $approvedCount); ?></strong>
        <p>Approved voters</p>
    </article>
    <article class="stat-box">
        <strong><?= e((string) $usedTokenCount); ?></strong>
        <p>Used tokens</p>
    </article>
    <article class="stat-box">
        <strong><?= e($snapshot ? substr($snapshot['integrity_hash'], 0, 10) . '...' : 'None'); ?></strong>
        <p>Latest snapshot hash</p>
    </article>
</section>

<section class="grid-2">
    <article class="results-strip">
        <span class="eyebrow">Live Tally</span>
        <h2><?= e($event['title']); ?></h2>
        <?php foreach ($results['rows'] as $row): ?>
            <div class="result-bar">
                <div class="list-row">
                    <div>
                        <strong><?= e($row['option_label']); ?></strong>
                        <p><?= e((string) $row['total_votes']); ?> votes</p>
                    </div>
                    <strong><?= e((string) $row['percentage']); ?>%</strong>
                </div>
                <div class="result-bar__track">
                    <div class="result-bar__fill" style="width: <?= e((string) $row['percentage']); ?>%"></div>
                </div>
            </div>
        <?php endforeach; ?>
        <form method="post">
            <?= csrf_field(); ?>
            <input type="hidden" name="event_id" value="<?= e((string) $eventId); ?>">
            <button class="button button--primary" type="submit">Generate snapshot</button>
        </form>
    </article>

    <article class="ledger">
        <span class="eyebrow">Snapshot</span>
        <h2>Latest integrity record</h2>
        <?php if ($snapshot): ?>
            <pre><?= e($snapshot['snapshot_json']); ?></pre>
            <div class="alert alert--info">Generated by <?= e($snapshot['generated_by_name']); ?> with hash <?= e($snapshot['integrity_hash']); ?></div>
        <?php else: ?>
            <div class="alert alert--warning">No snapshot has been generated yet.</div>
        <?php endif; ?>
    </article>
</section>
<?php include dirname(__DIR__) . '/includes/footer.php'; ?>
